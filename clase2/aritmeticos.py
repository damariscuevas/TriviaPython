# Ejemplos de operadores aritméticos

# Definimos dos variables
a = 10
b = 3

# Suma
resultado_suma = a + b
print(f"Suma: {a} + {b} = {resultado_suma}")

# Resta
resultado_resta = a - b 
print(f"Resta: {a} - {b} = {resultado_resta}")

# Multiplicación
resultado_multiplicacion = a * b
print(f"Multiplicacion: {a} * {b} = {resultado_multiplicacion}")

# División
resultado_division = a / b
print(f"Division: {a} / {b} = {resultado_division}")

# Divisón entera (Divide la parte entera del cociente)
resultado_division_entera = a // b
print(f"División entera = {a} // {b} = {resultado_division_entera}") 

# Módulo
resultado_modulo = a % b
print(f"Módulo = {a} % {b} = {resultado_modulo}")

# Exponente
resultado_exponente = a ** b
print(f"Resultado de exponente = {a} ** {b} = {resultado_exponente}")
