# Ejemplos de operadores de asignación
# Asignación Simple
x = 10
print(f"x = {x}")

# Suma y asignación
x += 5
print(f"x += 5: {x}")

# Resta y asignación
x -= 3
print(f"x -= 3: {x}")

# Mutiplicación y asignación
x *= 4 # Equivale a x = x * 4
print(f"x *= 4: {x}")

# División y asignación
x /= 2
print(f"x =/ 2: {x}")

# División entera y asignación
x //= 3
print(f"x //= 3: {x}")

# Módulo y asignación
x %= 7
print(f"x %= 7: {x}")

# Exponente y asignacion
x **= 2
print(f"x **= 2: {x}")
