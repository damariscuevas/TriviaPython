# Manejo de Matrículas en una Tupla
matrículas = 101, 102, 103, 104, 105

matricula_busqueda = matrículas.count(102)
print(f"Número de veces que aparece la matrícula 102 en la tupla:", matricula_busqueda)

ind_matricula = matrículas.index(104)
print(f"La matrícula 104 se encuentra en la posición ", ind_matricula, "de la tupla.")


